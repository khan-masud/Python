myTuple = ("Masud", 10, 93, True, "Python") # This is a tuple that is immutable like string. We can't insert or delete any element from tuples.

print(myTuple)

# Return the index of any element of tuples
print(myTuple.index("Masud")) # Output 0

# Return the total numbers of any element in tuples
print(myTuple.count("Python")) # Output 1